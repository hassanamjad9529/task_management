class UserModel {
  final String uid;
  final String email;
  final String name;
  final String role;
  final String designation;
  final double salary;
  final String phoneNumber;

  UserModel({
    required this.uid,
    required this.email,
    required this.name,
    required this.role,
    required this.designation,
    required this.salary,
    required this.phoneNumber,
  });
}
